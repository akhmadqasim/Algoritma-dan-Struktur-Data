# Fungsi ord() untuk mengubah karakter menjadi nilai ordinal
# ordinal adalah nilai yang merepresentasikan posisi karakter dalam tabel ASCII

print("Nilai ordinal dari 'c' adalah", ord('c'))  # mengubah karakter menjadi nilai ordinal
print("Nilai ordinal dari 'a' adalah", ord('a'))  # mengubah karakter menjadi nilai ordinal
print("Nilai ordinal dari 'r' adalah", ord('r'))  # mengubah karakter menjadi nilai ordinal
print("Nilai ordinal dari 'i' adalah", ord('i'))  # mengubah karakter menjadi nilai ordinal
print("Nilai ordinal dari '?' adalah", ord('?'))  # mengubah karakter menjadi nilai ordinal
