# rumus luas lingkaran
def luasLingkaran(r):  # Membuat fungsi luasLingkaran dengan parameter r
    return 3.14 * r ** 2  # Mengembalikan nilai (3.14 * r pangkat 2)


# rumus luas segitiga siku-siku
def luasSegitiga(a, t):  # Membuat fungsi luasSegitiga dengan parameter a dan t
    return 0.5 * a * t  # Mengembalikan nilai (0.5 * a * t)
