counter = 0  # Memberi nilai 0 pada variabel counter
while counter < 10:  # Melakukan perulangan selama nilai counter kurang dari 10
    print(counter, "Kurang dari 10")  # Menampilkan variabel counter Kurang dari 10
    counter = counter + 1  # Menambahkan nilai 1 pada variabel counter
else:  # Jika perulangan selesai
    print(counter, "Sama dengan 10")  # Menampilkan teks counter Sama dengan 10
